public class Espece {

    private String nom;

    private String cri;



    public Espece(String sonNom, String sonCri) {

        this.nom = sonNom;

        this.cri = sonCri;

    }



    public String getNom() {

        return this.nom;

    }


    public String getCri() {

        return this.cri;

    }

}
